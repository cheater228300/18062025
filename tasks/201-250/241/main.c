// Задача 241Напишите программу, которая выделяет память под строку, 
//запрашивает у пользователя текст, записывает его в строку и после 
//завершения освобождает память. 
#include <stdio.h>
#include <stdlib.h>

int main() {
    // Выделяем память под строку (например, максимальная длина 255 символов + '\0')
    char *str = (char *)malloc(256 * sizeof(char));
    
    if (str == NULL) {
        printf("Ошибка выделения памяти!\n");
        return 1;
    }
    
    // Запрашиваем текст у пользователя
    printf("Введите текст (максимум 255 символов): ");
    fgets(str, 256, stdin);
    
    // Выводим введенный текст
    printf("Вы ввели: %s", str);
    
    // Освобождаем память
    free(str);
    
    return 0;
}
