/*
Условие задачи 105:
105. Создайте программу, которая определяет количество цифр в числе, введённом пользователем.
*/

#include <stdio.h>

int main() {
    long long num;
    int count = 0;

    printf("Введите число: ");
    scanf("%lld", &num);

    if (num == 0) {
        count = 1;  // Ноль состоит из одной цифры
    } else {
        if (num < 0) {
            num = -num;  // Сделаем число положительным для подсчёта цифр
        }
        while (num > 0) {
            num /= 10;
            count++;
        }
    }

    printf("Количество цифр в числе: %d\n", count);

    return 0;
}
