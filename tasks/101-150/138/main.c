//гробик

#include <stdio.h>

// Функция reverse_array переворачивает массив целых чисел на месте.
// Параметры:
//   arr - указатель на первый элемент массива
//   n   - количество элементов в массиве
void reverse_array(int *arr, int n) {
    // Инициализируем два указателя:
    // start указывает на первый элемент,
    // end указывает на последний элемент массива
    int *start = arr;
    int *end = arr + n - 1;

    // Цикл продолжается, пока указатель start меньше end,
    // то есть пока не встретились посередине массива
    while (start < end) {
        // Меняем значения элементов, на которые указывают start и end
        int temp = *start;  // сохраняем значение первого элемента
        *start = *end;      // копируем значение последнего в первый
        *end = temp;        // записываем сохранённое значение в последний

        // Сдвигаем указатели навстречу друг другу
        start++;
        end--;
    }
}

int main() {
    // Инициализация массива целых чисел
    int data[] = {1, 2, 3, 4, 5};
    // Вычисляем количество элементов массива
    int n = sizeof(data) / sizeof(data[0]);

    // Выводим массив до переворота
    printf("До переворота: ");
    for (int i = 0; i < n; i++) {
        printf("%d ", data[i]);
    }
    printf("\n");

    // Вызываем функцию для переворота массива
    reverse_array(data, n);

    // Выводим массив после переворота
    printf("После переворота: ");
    for (int i = 0; i < n; i++) {
        printf("%d ", data[i]);
    }
    printf("\n");

    return 0;
}

