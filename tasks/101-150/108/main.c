/*
Условие задачи 108:
108. Создайте программу, которая подсчитывает сумму только чётных чисел, введённых пользователем. Программа завершается при вводе 0.
*/

#include <stdio.h>

int main() {
    int num;
    int sum = 0;

    printf("Введите числа (0 для завершения):\n");

    while (1) {
        scanf("%d", &num);
        if (num == 0) {
            break;
        }
        if (num % 2 == 0) {
            sum += num;
        }
    }

    printf("Сумма чётных чисел: %d\n", sum);

    return 0;
}
