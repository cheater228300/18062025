// Объявите переменные типов int, float, char, Присвойте значения переменным и выведите их значения на экран.
// Решение: 
#include <stdio.h>
int main (){
    int INT;
    float FLOAT;
    char CHAR;
    INT = 10;
    FLOAT = 10.1;
    CHAR = 'X';
    printf("%d\n",INT);
    printf("%f\n",FLOAT);
    printf("%c\n",CHAR);
    return 0;
}
