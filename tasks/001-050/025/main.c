/*
Условие задачи 025:
25. Объявите переменную int a = 42; и указатель int *p = &a;. Измените значение переменной через указатель и выведите оба значения до и после изменения.
*/
#include <stdio.h>

int main() {
    int a = 42;
    int *p = &a;

    printf("До изменения: a = %d, *p = %d\n", a, *p);

    *p = 100;  // изменяем значение через указатель

    printf("После изменения: a = %d, *p = %d\n", a, *p);

    return 0;
}
