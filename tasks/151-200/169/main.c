#include <stdio.h>

int main() {
    int arr[] = {10, 20, 30, 40, 50};
    int *ptr = arr;  // указатель на первый элемент массива

    printf("Элементы массива через указатель и адресную арифметику:\n");

    for (int i = 0; i < 5; i++) {
        printf("Элемент %d: значение = %d, адрес = %p\n", i, *(ptr + i), (ptr + i));
    }

    // Демонстрация изменения значения через указатель
    *(ptr + 2) = 100;
    printf("\nПосле изменения третьего элемента через указатель:\n");
    for (int i = 0; i < 5; i++) {
        printf("Элемент %d: %d\n", i, arr[i]);
    }

    return 0;
}
